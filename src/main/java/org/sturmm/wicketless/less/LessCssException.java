package org.sturmm.wicketless.less;

public class LessCssException extends RuntimeException {
    private static final long serialVersionUID = 26016767918781830L;

    public LessCssException() {
        super();
    }

    public LessCssException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public LessCssException(String arg0) {
        super(arg0);
    }

    public LessCssException(Throwable arg0) {
        super(arg0);
    }

}
