package org.sturmm.wicketless.less;

public class LessResourceException extends LessCssException {
    private static final long serialVersionUID = 26016767918781830L;

    public LessResourceException() {
        super();
    }

    public LessResourceException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public LessResourceException(String arg0) {
        super(arg0);
    }

    public LessResourceException(Throwable arg0) {
        super(arg0);
    }

}
